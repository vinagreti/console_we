        <p>A ordem de um canal define como este canal será exibido nas consultas ao aplicativo. Possibilitando posicionar conteúdo mais relevante no topo das pesquisas melhorando o acesso dos leitores.</p>
        <p>Para alterar a ordem de um canal basta clicar nos botões de “+” (soma)  e “-” (subtração) localizados na coluna “Ordem” da lista de canais em <a href="<?=base_url()?>/canais">Canais</a>.</p>
