        <p>No módulo <a href="<?=base_url()?>usuarios" target="_blank">Usuários Administrativos</a> é possível gerênciar os <a href="<?=base_url()?>usuarios" target="_blank">Usuários Administrativos</a> de cada Console. Nele podemos:</p>

        <ol>
          <li>Listar os usuários cadastrados;</li>
          <li>Cadastrar novos usuários;</li>
          <li>Editar as informações dos usuários cadastrados;</li>
          <li>Excluir usuários cadastrados;</li>
          <li>Alterar o grupo de um usuário;</li>
        </ol>

        <p><strong>1 - Listando os usuários</strong><p>
        <p>Para ver a lista de usuários cadastrados, basta acessar o módulo <a href="<?=base_url()?>usuarios" target="_blank">Usuários Administrativos</a>.</p>

        <p><strong>2 - Cadastrando um novo usuário</strong><p>
        <p>Para cadastrar um usuário primeiro devemos clicar no botão “Criar novo usuário”, localizado acima da lista de usuários administrativos em <a href="<?=base_url()?>usuarios" target="_blank">Usuários Administrativos</a>.</p>
        <p>Em seguida será exibido um formulário onde deverão ser inseridos os dados do novo usuário.</p>

        <ul>
          <li>Administrador: define se o novo usuário terá permissão para administrar outros usuários.</li>
          <li>Grupo: define o grupo do usuário. Os grupos são utilizados para definir as permissões dos usuários dentro do Console.</li>
          <li>Após inserir os dados do usuário, é preciso clicar no botão “Criar” para finalizar o cadastro.</li>
        </ul>

        <p><strong>3 - Editando as informações de um usuário</strong><p>
        <p>Para editar as informações de um usuário primeiro devemos clicar no botão “Editar usuário”, localizado no canto direito de cada linha da lista de usuários administrativos em <a href="<?=base_url()?>usuarios" target="_blank">Usuários Administrativos</a>.</p>
        <p>Ps.: Para ver o botão de editar, é preciso passar o mouse sobre a linha do usuário que deseja editar. Na coluna “Ações” aparecerá o ícone de um lápis. Clicar neste ícone para editar o usuário.</p>
        <p>Em seguida será exibido um formulário onde deverão ser alterados os dados do usuário.</p>

        <ul>
          <li>Administrador: define se o novo usuário terá permissão para administrar outros usuários.</li>
          <li>Grupo: define o grupo do usuário. Os grupos são utilizados para definir as permissões dos usuários dentro do Console.</li>
          <li>Após inserir os dados do usuário, é preciso clicar no botão “Editar” para finalizar a edição.</li>
        </ul>

        <p><strong>4 - Excluindo um usuário</strong><p>
        <p>Para excluir um usuário primeiro devemos clicar no botão “Excluir usuário”, localizado no canto direito de cada linha da lista de usuários administrativos em <a href="<?=base_url()?>usuarios" target="_blank">Usuários Administrativos</a>.</p>
        <p>Ps.: Para ver o botão de exclusão, é preciso passar o mouse sobre a linha do usuário que deseja excluir. Na coluna “Ações” aparecerá o ícone de uma lixeira. Clicar neste ícone para excluir o usuário.</p>
        <p>Em seguida será exibida uma caixa de diálogo solicitando a confirmação da solicitação de exclusão. Para finalizar a exclusão, é preciso clicar no botão “Ok”.</p>

