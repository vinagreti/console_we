        <p>Após inserir a senha e a confirmação, é preciso clicar no botão “Alterar senha” para concluir a alteração.</p>

        <p><strong>2 - Alterando senha pela pela Recuperação de Senha</strong></p>
        <p>Para alterar a senha pela recuperação de senha primeiro devemos acessar a tela de Login onde será exibido um formulário de autenticação. Neste formulário existe um link chamado “Recuperar senha”. Devemos clicar no link para abrir o formulário de recuperação de senha.</p>
        <p>Em seguida será exibido um formulário onde deveremos informar o e-mail cadastrado na conta.</p>
        <p>Para <a href="<?=base_url()?>recuperarSenha" target="_blank">recuperar a senha</a> é preciso clicar no botão “Recuperar senha”.</p>
