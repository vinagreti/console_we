        <p>O Console possui a opção de <a href="<?=base_url()?>alterarSenha" target="_blank">Alterar a senha</a> dos usuários.</p>

        <p>Para alterar a senha, temos duas opções:</p>

        <ol>
          <li><a href="<?=base_url()?>alterarSenha" target="_blank">Alterar a senha</a> pelo menú de configurações;</li>
          <li>Recuperar a senha.</li>
        </ol>

        <p><strong>1 - Alterando a senha pelo menú de configurações</strong></p>
        <p>Podemos acessar o formulário de alteração de senha através do menú localizado no canto superior direito da tela. Um ícone em forma de engrenagem.</p>
